#!/usr/bin/env node
/**
 * One-shot Keystatic GitHub App setup (manifest flow).
 *
 * Usage:
 *   node scripts/setup-keystatic-github.mjs
 *
 * Opens a browser → you approve the GitHub App → credentials land in
 * `.env.keystatic.local` (gitignored). Then follow printed Netlify steps.
 */
import http from 'node:http'
import { writeFileSync, readFileSync, existsSync } from 'node:fs'
import { createHash, randomBytes } from 'node:crypto'
import { execSync } from 'node:child_process'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const PORT = 9876
const REDIRECT = `http://127.0.0.1:${PORT}/callback`
const SITE = 'https://razet.me'
const REPO = 'priority3/priority.me'
const APP_NAME = 'priority-me-keystatic'

const state = randomBytes(16).toString('hex')
const secret = randomBytes(32).toString('hex')

const manifest = {
  name: APP_NAME,
  url: SITE,
  redirect_url: REDIRECT,
  callback_urls: [
    `${SITE}/api/keystatic/github/oauth/callback`,
    'http://127.0.0.1:4321/api/keystatic/github/oauth/callback',
    'http://localhost:4321/api/keystatic/github/oauth/callback',
  ],
  // Webhook required by API shape but unused by Keystatic
  hook_attributes: {
    url: `${SITE}/api/keystatic/github/webhook-unused`,
    active: false,
  },
  public: false,
  default_permissions: {
    contents: 'write',
    metadata: 'read',
    pull_requests: 'write',
  },
}

function openBrowser(url) {
  const platform = process.platform
  try {
    if (platform === 'darwin')
      execSync(`open "${url}"`)
    else if (platform === 'win32')
      execSync(`start "" "${url}"`)
    else
      execSync(`xdg-open "${url}"`)
  }
  catch {
    console.log('\nOpen this URL manually:\n', url)
  }
}

function writeEnvFile({ clientId, clientSecret, slug }) {
  const body = `# Generated by scripts/setup-keystatic-github.mjs — DO NOT COMMIT
# Paste these into Netlify → Site configuration → Environment variables

KEYSTATIC_STORAGE=github
KEYSTATIC_GITHUB_CLIENT_ID=${clientId}
KEYSTATIC_GITHUB_CLIENT_SECRET=${clientSecret}
KEYSTATIC_SECRET=${secret}
PUBLIC_KEYSTATIC_GITHUB_APP_SLUG=${slug}
`

  const out = join(root, '.env.keystatic.local')
  writeFileSync(out, body, 'utf8')

  // Merge into .env for local github-mode testing (optional)
  const envPath = join(root, '.env')
  let existing = existsSync(envPath) ? readFileSync(envPath, 'utf8') : ''
  // strip previous keystatic block
  existing = existing.replace(/\n?# --- Keystatic \(auto\).*?(?=\n# ---|\n*$)/s, '\n')
  const block = `
# --- Keystatic (auto) ---
KEYSTATIC_GITHUB_CLIENT_ID=${clientId}
KEYSTATIC_GITHUB_CLIENT_SECRET=${clientSecret}
KEYSTATIC_SECRET=${secret}
PUBLIC_KEYSTATIC_GITHUB_APP_SLUG=${slug}
`
  writeFileSync(envPath, `${existing.trim()}\n${block}\n`, 'utf8')

  return out
}

async function convertManifest(code) {
  const res = await fetch(`https://api.github.com/app-manifests/${code}/conversions`, {
    method: 'POST',
    headers: {
      Accept: 'application/vnd.github+json',
      'X-GitHub-Api-Version': '2022-11-28',
      'User-Agent': 'priority-me-keystatic-setup',
    },
  })
  if (!res.ok) {
    const text = await res.text()
    throw new Error(`Manifest conversion failed (${res.status}): ${text}`)
  }
  return res.json()
}

const server = http.createServer(async (req, res) => {
  const url = new URL(req.url || '/', `http://127.0.0.1:${PORT}`)

  if (url.pathname === '/') {
    res.writeHead(200, { 'Content-Type': 'text/html; charset=utf-8' })
    res.end(`<!doctype html>
<html><body style="font-family:system-ui;padding:2rem;max-width:40rem">
  <h1>Create Keystatic GitHub App</h1>
  <p>Click below, review permissions, then Create GitHub App.</p>
  <form action="https://github.com/settings/apps/new?state=${state}" method="post">
    <input type="hidden" name="manifest" value='${JSON.stringify(manifest).replace(/'/g, '&#39;')}' />
    <button type="submit" style="font-size:1.1rem;padding:.6rem 1.2rem;cursor:pointer">
      Create GitHub App on GitHub
    </button>
  </form>
</body></html>`)
    return
  }

  if (url.pathname === '/callback') {
    const code = url.searchParams.get('code')
    const returnedState = url.searchParams.get('state')
    if (!code) {
      res.writeHead(400, { 'Content-Type': 'text/plain' })
      res.end('Missing code')
      return
    }
    if (returnedState && returnedState !== state) {
      res.writeHead(400, { 'Content-Type': 'text/plain' })
      res.end('Invalid state')
      return
    }

    try {
      const app = await convertManifest(code)
      const clientId = app.client_id
      const clientSecret = app.client_secret
      const slug = app.slug

      const out = writeEnvFile({ clientId, clientSecret, slug })
      const installUrl = `https://github.com/apps/${slug}/installations/new`

      console.log('\n✅ GitHub App created:', slug)
      console.log('   Client ID:', clientId)
      console.log('   Credentials written to:', out)
      console.log('   Also merged into .env (gitignored)')
      console.log('\n👉 Next: install the App on the repo:')
      console.log('  ', installUrl)
      console.log('   Choose Only select repositories → priority.me\n')

      res.writeHead(200, { 'Content-Type': 'text/html; charset=utf-8' })
      res.end(`<!doctype html>
<html><body style="font-family:system-ui;padding:2rem;max-width:40rem">
  <h1>✅ App created</h1>
  <p><strong>Slug:</strong> ${slug}</p>
  <p>Credentials saved to <code>.env.keystatic.local</code> and <code>.env</code>.</p>
  <p><a href="${installUrl}" style="font-size:1.1rem">Install App on priority.me →</a></p>
  <p>After install, set the same env vars on Netlify (see terminal), then redeploy.</p>
  <p>You can close this tab.</p>
</body></html>`)

      openBrowser(installUrl)

      setTimeout(() => {
        server.close()
        printNetlifyHelp({ clientId, clientSecret, slug, secret })
        process.exit(0)
      }, 500)
    }
    catch (err) {
      console.error(err)
      res.writeHead(500, { 'Content-Type': 'text/plain' })
      res.end(String(err))
    }
    return
  }

  res.writeHead(404)
  res.end('Not found')
})

function printNetlifyHelp({ clientId, clientSecret, slug, secret }) {
  console.log(`
════════════════════════════════════════════════════════
Netlify environment variables (Site settings → Env vars)
════════════════════════════════════════════════════════

KEYSTATIC_GITHUB_CLIENT_ID=${clientId}
KEYSTATIC_GITHUB_CLIENT_SECRET=${clientSecret}
KEYSTATIC_SECRET=${secret}
PUBLIC_KEYSTATIC_GITHUB_APP_SLUG=${slug}

Then: trigger a new deploy.

Online admin:
  ${SITE}/keystatic

Local still uses disk storage (KEYSTATIC_STORAGE unset + NODE_ENV=development).
To force GitHub mode locally: KEYSTATIC_STORAGE=github pnpm dev
`)
}

server.listen(PORT, '127.0.0.1', () => {
  const url = `http://127.0.0.1:${PORT}/`
  console.log(`
Keystatic GitHub App setup
──────────────────────────
1. Browser will open: ${url}
2. Click "Create GitHub App on GitHub" and approve
3. Install the app on ${REPO}
4. Copy env vars to Netlify (printed at the end)

Waiting for GitHub callback on ${REDIRECT} …
`)
  openBrowser(url)
})
